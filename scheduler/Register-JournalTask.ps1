<#
.SYNOPSIS
    Registers the nightly Claude Code -> OneNote journal as a Windows scheduled task.

.DESCRIPTION
    Creates (or replaces) a Task Scheduler job that, at 23:59 local time each
    day, runs `claude -p` with the OneNote summarizer prompt. The summarizer
    reads that day's mirrored session transcripts (and any git pushes) and
    publishes a OneNote page for the day.

    The task runs in the current user's context (so it inherits the same Claude
    Code auth and the cert in CurrentUser\My). It is set to start as soon as
    possible after a missed start, so if the PC is asleep/off at 23:59 the run
    happens at the next wake.

.PARAMETER TaskName
    Name of the scheduled task. Default: "Claude OneNote Journal".

.PARAMETER Time
    Daily start time (HH:mm). Default: "23:59".

.PARAMETER ClaudePath
    Full path to the claude executable. Auto-detected from PATH if omitted.

.PARAMETER Unregister
    Remove the task instead of creating it.

.EXAMPLE
    .\Register-JournalTask.ps1
    Registers the task at 23:59 daily.

.EXAMPLE
    .\Register-JournalTask.ps1 -Time "22:30"
    Registers the task at 22:30 daily.

.EXAMPLE
    .\Register-JournalTask.ps1 -Unregister
    Removes the task.

.NOTES
    Part of obsidian-claude-code-journal. MIT licensed.
    Version: 1.0
#>

#Requires -Version 7.0

param(
    [string]$TaskName   = "Claude OneNote Journal",
    [string]$Time       = "23:59",
    [string]$ClaudePath,
    [switch]$Unregister
)

$ErrorActionPreference = "Stop"

$repoRoot   = Split-Path $PSScriptRoot -Parent
$promptPath = Join-Path $PSScriptRoot "nightly-journal-prompt.md"
$logDir     = Join-Path $repoRoot "logs"
$runnerPath = Join-Path $PSScriptRoot "Run-NightlyJournal.ps1"

if ($Unregister) {
    if (Get-ScheduledTask -TaskName $TaskName -ErrorAction SilentlyContinue) {
        Unregister-ScheduledTask -TaskName $TaskName -Confirm:$false
        Write-Host "OK  Removed scheduled task '$TaskName'." -ForegroundColor Green
    } else {
        Write-Host "i   No task named '$TaskName' found." -ForegroundColor Cyan
    }
    return
}

# Resolve claude executable
if (-not $ClaudePath) {
    $cmd = Get-Command claude -ErrorAction SilentlyContinue
    if (-not $cmd) { throw "Could not find 'claude' on PATH. Pass -ClaudePath explicitly." }
    $ClaudePath = $cmd.Source
}
if (-not (Test-Path $promptPath)) { throw "Prompt not found: $promptPath" }
New-Item -ItemType Directory -Path $logDir -Force | Out-Null

# A small wrapper runner keeps the scheduled-task action simple and logs output.
$runner = @'
#Requires -Version 7.0
# Auto-generated by Register-JournalTask.ps1 — runs the nightly OneNote journal.
param(
    [string]$ClaudePath = "{{CLAUDE}}",
    [string]$PromptPath = "{{PROMPT}}",
    [string]$LogDir     = "{{LOGDIR}}"
)
$ErrorActionPreference = "Continue"
$stamp = Get-Date -Format 'yyyy-MM-dd'
$log   = Join-Path $LogDir "journal-$stamp.log"
$prompt = Get-Content -Path $PromptPath -Raw -Encoding UTF8
"[$(Get-Date -Format s)] Starting nightly journal run" | Out-File $log -Append -Encoding UTF8
# Headless, non-interactive. --permission-mode acceptEdits lets it run the
# publisher and write the temp HTML without prompting.
& $ClaudePath -p $prompt --permission-mode acceptEdits *>&1 | Out-File $log -Append -Encoding UTF8
"[$(Get-Date -Format s)] Finished (exit $LASTEXITCODE)" | Out-File $log -Append -Encoding UTF8
'@
$runner = $runner.Replace("{{CLAUDE}}", $ClaudePath).Replace("{{PROMPT}}", $promptPath).Replace("{{LOGDIR}}", $logDir)
Set-Content -Path $runnerPath -Value $runner -Encoding UTF8
Write-Host "OK  Wrote runner: $runnerPath" -ForegroundColor Green

# pwsh.exe to host the runner
$pwsh = (Get-Command pwsh -ErrorAction SilentlyContinue).Source
if (-not $pwsh) { $pwsh = (Get-Command powershell).Source }

$action = New-ScheduledTaskAction -Execute $pwsh `
    -Argument "-NoProfile -ExecutionPolicy Bypass -File `"$runnerPath`""

$trigger = New-ScheduledTaskTrigger -Daily -At $Time

$settings = New-ScheduledTaskSettingsSet `
    -StartWhenAvailable `
    -DontStopOnIdleEnd `
    -ExecutionTimeLimit (New-TimeSpan -Hours 1) `
    -MultipleInstances IgnoreNew

# Run as the current user, only when logged on (so it shares your Claude auth
# and the CurrentUser cert store). Interactive token, no stored password.
$principal = New-ScheduledTaskPrincipal -UserId "$env:USERDOMAIN\$env:USERNAME" -LogonType Interactive -RunLevel Limited

if (Get-ScheduledTask -TaskName $TaskName -ErrorAction SilentlyContinue) {
    Unregister-ScheduledTask -TaskName $TaskName -Confirm:$false
    Write-Host "i   Replaced existing task '$TaskName'." -ForegroundColor Cyan
}

Register-ScheduledTask -TaskName $TaskName -Action $action -Trigger $trigger `
    -Settings $settings -Principal $principal `
    -Description "Nightly: summarize the day's Claude Code sessions and publish to OneNote." | Out-Null

Write-Host "OK  Registered '$TaskName' to run daily at $Time." -ForegroundColor Green
Write-Host "i   Logs: $logDir\journal-<date>.log" -ForegroundColor Cyan
Write-Host "i   Test now:  Start-ScheduledTask -TaskName `"$TaskName`"" -ForegroundColor Cyan
